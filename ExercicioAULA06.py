def calcular(a, b):
    return a * b

# Exemplo de uso da função:
resultado = calcular(3, 4)
print(resultado)  # Saída: 12

def verificar_maior_que_500(valor):
    if valor >= 500:
        print("é maior")
    else:
        print("não é maior")

# Exemplo de uso da função:
verificar_maior_que_500(600)  # Saída: é maior
verificar_maior_que_500(400)  # Saída: não é maior
